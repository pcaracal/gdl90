use std::time::Duration;

use crate::prelude::*;

const PAYLOAD_LEN: usize = 432;

#[derive(Debug, Clone, Copy, PartialEq, Eq, DekuRead, DekuWrite, Builder)]
#[deku(bit_order = "msb", endian = "big")]
/// # 3.3. Uplink Data Message
pub struct UplinkData {
    /// 3.3.1. Time of Reception (TOR)
    ///
    /// None = invalid (default)
    #[deku(
        reader = "UplinkData::tor_read(deku::reader)",
        writer = "UplinkData::tor_write(deku::writer, self.time_of_reception)"
    )]
    pub time_of_reception: Option<Duration>,

    /// 3.3.2. Uplink Payload
    #[builder(default = [0;432], setter = |data: impl AsRef<[u8]>| { self.uplink_payload[..data.as_ref().len().min(PAYLOAD_LEN)].copy_from_slice(&data.as_ref()[..data.as_ref().len().min(PAYLOAD_LEN)]); })]
    pub uplink_payload: [u8; PAYLOAD_LEN],
}

impl Default for UplinkData {
    fn default() -> Self {
        Self {
            time_of_reception: None,
            uplink_payload: [0; 432],
        }
    }
}

impl UplinkData {
    const TOR_MAX: u32 = 12_499_999;
    const TOR_INVALID: u32 = 0xFF_FF_FF;
    const TOR_CTX: (Endian, ByteSize) = (Endian::Little, ByteSize(3));
    const TOR_RESOLUTION_NS: u32 = 80;
    fn tor_read<R: std::io::Read + std::io::Seek>(
        reader: &mut deku::reader::Reader<R>,
    ) -> Result<Option<Duration>, DekuError> {
        let tor = u32::from_reader_with_ctx(reader, Self::TOR_CTX)?;
        Ok(if tor == Self::TOR_INVALID {
            None
        } else {
            Some(Duration::from_nanos(
                (tor.min(Self::TOR_MAX) * Self::TOR_RESOLUTION_NS).into(),
            ))
        })
    }
    fn tor_write<W: std::io::Write + std::io::Seek>(
        writer: &mut deku::writer::Writer<W>,
        tor: Option<Duration>,
    ) -> Result<(), DekuError> {
        let encoded = if let Some(tor) = tor {
            let tor_ns = tor.subsec_nanos();
            (tor_ns / Self::TOR_RESOLUTION_NS).min(Self::TOR_MAX)
        } else {
            Self::TOR_INVALID
        };
        encoded.to_writer(writer, Self::TOR_CTX)
    }
}

