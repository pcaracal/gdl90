    #[test]
    fn decode() {
        let ud = UplinkData::from_bytes((&BYTES, 0)).unwrap().1;

        assert!(ud.time_of_reception.is_none());
        assert_eq!(ud.uplink_payload, BYTES[3..]);

        let mut bytes2 = BYTES.to_vec();
        bytes2[0..3].copy_from_slice(&[0x12, 0x34, 0x56]);
        let (_, ud2) = UplinkData::from_bytes((&bytes2, 0)).unwrap();
        assert!(ud2.time_of_reception.is_some());
        assert_eq!(ud2.time_of_reception.unwrap().as_nanos(), 0x56_34_12 * 80);
        assert_eq!(
            ud2.time_of_reception.unwrap(),
            Duration::from_nanos(451_954_080)
        );
    }

    #[test]
    fn encode() {
        let ud = UplinkData::default().with_uplink_payload(&BYTES[3..]);

        assert!(ud.time_of_reception.is_none());
        assert_eq!(ud.uplink_payload, BYTES[3..]);

        let bytes = ud.to_bytes().unwrap();
        assert_eq!(bytes, BYTES);
    }

    #[test]
    fn uplink_payload_no_panic() {
        let data = [0xFF; PAYLOAD_LEN + 10];
        let ud = UplinkData::default().with_uplink_payload(data);
        assert_eq!(ud.uplink_payload, [0xFF; PAYLOAD_LEN]);

        let data = [0xFF; PAYLOAD_LEN - 10];
        let ud = UplinkData::default().with_uplink_payload(data);
        assert_eq!(
            ud.uplink_payload[..data.len()],
            [0xFF; PAYLOAD_LEN - 10][..]
        );

        let ud = UplinkData::default().with_uplink_payload([]);
        assert_eq!(ud.uplink_payload, [0; PAYLOAD_LEN]);
    }
